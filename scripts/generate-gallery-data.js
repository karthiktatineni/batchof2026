const fs = require('fs');
const path = require('path');

const baseDir = path.join(process.cwd(), 'public');

const folders = [
  {
    name: 'First year photos or any td pics in specified years (File responses)',
    category: '1st Year',
    prefix: 'y1'
  },
  {
    name: 'Second  year photos or any td pics in specified years (File responses)',
    category: '2nd Year',
    prefix: 'y2'
  },
  {
    name: 'Third year photos or any td pics in specified years (File responses)',
    category: '3rd Year',
    prefix: 'y3'
  },
  {
    name: 'Fourth year photos or any td pics in specified years (File responses)',
    category: '4th Year',
    prefix: 'y4'
  }
];

// Track canonical filenames globally to prevent cross-folder dupes
// Strip trailing (1), (2) etc. suffixes + extension to get base name
// Files with same base name + same size = true duplicate
function getBaseName(filename) {
  const ext = path.extname(filename);
  const name = path.basename(filename, ext);
  // Remove trailing " (1)", " (2)", "(1)", "(2)" patterns
  return name.replace(/\s*\(\d+\)$/, '').trim();
}

const supportedExts = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.JPG', '.JPEG', '.PNG']);

const allImages = [];
// key: "baseName|||category" -> true
const seenKeys = new Set();
let idCounter = 0;

folders.forEach(folder => {
  const dirPath = path.join(baseDir, folder.name);
  if (!fs.existsSync(dirPath)) {
    console.error(`MISSING folder: ${folder.name}`);
    return;
  }

  const files = fs.readdirSync(dirPath);
  files.forEach(file => {
    const ext = path.extname(file).toLowerCase();
    // Skip HEIC/HEIF - browsers can't display them
    if (ext === '.heic' || ext === '.heif') return;
    // Only supported image types
    if (!['.jpg', '.jpeg', '.png', '.webp', '.gif'].includes(ext)) return;

    const baseName = getBaseName(file);
    // Dedup key: same base name within SAME category folder => duplicate
    const key = `${folder.category}|||${baseName}`;
    if (seenKeys.has(key)) return;
    seenKeys.add(key);

    // Build URL path - need to encode special chars
    const urlPath = `/${folder.name}/${file}`;

    // Extract contributor name after " - " and before extension
    let title = 'Memory';
    const dashIdx = file.lastIndexOf(' - ');
    if (dashIdx !== -1) {
      title = path.basename(file.substring(dashIdx + 3), path.extname(file));
      // Clean up (1), (2) from title too
      title = title.replace(/\s*\(\d+\)$/, '').trim();
    }

    allImages.push({
      id: `${folder.prefix}_${idCounter++}`,
      src: urlPath,
      category: folder.category,
      title
    });
  });
});

// Write output
const output = `// AUTO-GENERATED by scripts/generate-gallery-data.js
// Do not edit manually

export type YearCategory = '1st Year' | '2nd Year' | '3rd Year' | '4th Year';

export interface GalleryImage {
  id: string;
  src: string;
  category: YearCategory;
  title: string;
}

export const galleryImages: GalleryImage[] = ${JSON.stringify(allImages, null, 2)};
`;

const outPath = path.join(process.cwd(), 'src', 'lib', 'gallery-data.ts');
fs.writeFileSync(outPath, output, 'utf8');
console.log(`\nGenerated ${allImages.length} images -> src/lib/gallery-data.ts`);

const byYear = {};
allImages.forEach(img => {
  byYear[img.category] = (byYear[img.category] || 0) + 1;
});
Object.entries(byYear).forEach(([cat, count]) => {
  console.log(`  ${cat}: ${count} images`);
});
